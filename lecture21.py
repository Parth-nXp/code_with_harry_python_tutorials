"""
Operators: There are following types of operators in Python
1. Arithmetic operators
2. Assignment operators
3. Comparison operators
4. Logical operators
5. Identity operators
6. Membership operators
7. Bitwise operators
"""

"""Arithmetic operators"""
print("5 + 6 is ", 5 + 6)  # addition operator
print("5 - 6 is ", 5 - 6)  # subtraction operator
print("5 * 6 is ", 5 * 6)  # multiplication operator
print("5 / 6 is ", 5 / 6)  # division operator
print("15 // 6 is ", 15 // 6)  # floor division operator. Integer division
print("15 % 6 is ", 15 % 6)  # modulus operator. Gives reminder of the division
print("5 ** 3 is ", 5 ** 3)  # exponential operator

"""Assignment operator"""
x = 5  # assign value of 5 to x
x += 1  # adds 1 to the value of x

"""Comparison opertor"""
i = 8
print(i == 5)  # equal to operator
print(i < 5)  # less then operator
print(i > 5)  # greater then operator
print(i <= 5)  # less then equal to operator
print(i >= 5)  # greater then equal to operator
print(i != 5)  # not equal to operator
