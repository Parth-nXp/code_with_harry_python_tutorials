'''
# While loop
'''

i = 0  # Initialize counter

while i < 45:  # While loop condition where i is less than 45 (45 is the limit)
    print(i)  # Print the value of i
    i = i+1  # Increment counter by 1

# increment means to add 1 and decrement means to subtract 1 to the counter

# for loop vs while loop
# for loop is used when you know how many times you want to repeat a set of statements.
# while loop is used when you don't know how many times you want to repeat a set of statements. But you know when the loop will end.
# both loops are used to iterate over a sequence of items.
# it depends on the programmer how to use them.
