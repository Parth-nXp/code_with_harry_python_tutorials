'''
escape sequence characters
'''
print("Hello World", end="")  # end="" is used to print without newline
print("New Line", end=",")  # end="," is used to print with comma
# end is used to print with newline and is called newline character

print("C:\name")  # \n is a newline character
# to print \n in a string use \\n like this
print("C:\\name")  # \ is an escape character that ignores the next character
# \n is a newline character and \t is a tab charater which gives 6 spaces
print("He is \n a good \t boy")
